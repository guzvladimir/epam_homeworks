from typing import Any, Tuple


class SimplifiedEnum(type):
    def __new__(mcs: type, name: str, parents: Tuple, attr: dict) -> Any:
        new_attr = {key: key for key in attr[f"_{name}__keys"]}
        return super().__new__(mcs, name, parents, new_attr)
